export const NOTIF_GROUPS = ['ラウンド参加', 'マッチ・レビュー・再会', 'メッセージ・チャット', 'その他'];

const MAP: Record<string, NotifTemplate> = Object.fromEntries(NOTIF_TEMPLATES.map((t) => [t.key, t]));
export function getTemplateDef(key: string): NotifTemplate | undefined { return MAP[key]; }

// 保存された上書き（overrides）とデフォルトを突き合わせ、変数を差し込んで
// 4チャネル分の文面を返す。overrides は { key: { inApp?, line?, webTitle?, webBody? } }。
export type RenderedNotif = { inApp: string | null; line: string; webTitle: string; webBody: string };

function fill(s: string | undefined, vars: Record<string, string>): string {
  if (!s) return '';
  return s.replace(/\{([^}]+)\}/g, (_, k) => (k in vars ? String(vars[k] ?? '') : `{${k}}`));
}

export function renderFromOverrides(
  key: string,
  overrides: Record<string, NotifChannels> | undefined,
  vars: Record<string, string>,
): RenderedNotif {
  const def = MAP[key];
  const ov = (overrides && overrides[key]) || {};
  if (!def) return { inApp: null, line: '', webTitle: '', webBody: '' };
  const pick = (o?: string, d?: string) => (o != null && o !== '' ? o : d);
  return {
    inApp: def.noInApp ? null : fill(pick(ov.inApp, def.inApp), vars),
    line: fill(pick(ov.line, def.line), vars),
    webTitle: fill(pick(ov.webTitle, def.webTitle), vars),
    // スマホのプッシュ通知の本文は LINE通知（line）と共通。旧 webBody は参照しない。
    webBody: fill(pick(ov.line, def.line), vars),
  };
}
